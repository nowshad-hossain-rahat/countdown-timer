<h1>Short Code</h1>
<p>
    Copy this short-code and paste it anywhere into your WordPress page editor,
    Elementor page editor or in a widget.
    <br />Wherever you'll put this code, WordPress will display your Countdown Timer, Stopwatch, Days Counter there.
</p>
<hr>
<h3> Short Codes </h3>
<hr>

<label>For pre-created Countdown Timer :</label>
<code>[nhr-countdown-timer name="Timer Name"]</code>
<br />

<label>For Custom Countdown Timer :</label>
<code>[nhr-custom-countdown-timer]</code>
<br />

<label>For Days counter :</label>
<code>[nhr-days-counter]</code>
<br />

<label>For Days counter (with specific name for one field) :</label>
<code>[nhr-days-counter name="Any Name"]</code>
<br />

<label>For Stopwatch :</label>
<code>[nhr-stopwatch]</code>
<br />
