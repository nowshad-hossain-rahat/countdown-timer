<h1>Short Code</h1>
<p>
    Copy this short-code and paste it anywhere into your WordPress page editor, 
    Elementor page editor or in a widget.
    <br />Wherever you'll put this code, WordPress will display your 'Certificate Validator' form there.
</p>
<label> Short Code : </label>
<input type="text" value="[nhr-iacaab-certificate-validator-form]">
<br />
<p>
    Or you can copy this PHP code and paste it in any php file to display the form there.
</p>
<label>PHP Code : </label>
<input type="text" value="&lt;?php do_shortcode('[nhr-iacaab-certificate-validator-form]'); ?>" />